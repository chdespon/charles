#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*p1;
	const char	*p2;

	p1 = s1;
	p2 = s2;
	if (s1 == 0 && s2 == 0)
		return (s1);
	while (n--)
		p1[n] = p2[n];
	return (s1);
}
